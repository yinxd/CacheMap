package cndcsoft.android.map.lib;


/**
 * @author xiaox
 * 自定义点对象
 *
 */
public class Point {
	/// <summary>
    /// 构造函数
    /// </summary>
    /// <param name="x">x值</param>
    /// <param name="y">y值</param>
    public Point(double x, double y) { X = x; Y = y; }
    /// <summary>
    /// x值
    /// </summary>
    public double X;
    /// <summary>
    /// y值
    /// </summary>
    public double Y;
}
